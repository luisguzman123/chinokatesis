    private void cargarEquipoTrabajo() {
        Conexion cn = new Conexion();

        try {
            cn.conectar();
            ResultSet producto = cn.consultar("SELECT\n"
                    + "dt.cod_equipo_trabajo,\n"
                    + "et.fecha_inicio,\n"
                    + "et.fecha_final,\n"
                    + "e.emp_nom||' '||e.emp_ape as personal,\n"
                    + "ep.eta_desc\n"
                    + "\n"
                    + "FROM detalle_equipo_trabajo dt\n"
                    + "JOIN empleado e\n"
                    + "ON e.emp_id = dt.emp_id\n"
                    + "JOIN equipo_de_trabajo et\n"
                    + "ON et.cod_equipo_trabajo = dt.cod_equipo_trabajo\n"
                    + "JOIN etapas_produccion ep\n"
                    + "ON ep.cod_etapas = dt.cod_etapas\n"
                    + "\n"
                    + "WHERE dt.cod_or_prod =" + nroorden.getText()); //order by ordena de menor a mayor, si se quiere de mayor a menor se le agrega desc al final

            Metodos.limpiarTabla(grilla3);
            if (producto.isBeforeFirst()) {
                while (producto.next()) {
                    Metodos.cargarTabla(grilla3, new Object[]{
                        producto.getInt("cod_equipo_trabajo"),
                        Metodos.dameFechaFormateada(producto.getDate("fecha_inicio")),
                        Metodos.dameFechaFormateada(producto.getDate("fecha_final")),
                        producto.getString("personal"),
                        producto.getString("eta_desc")
                    });
                }
            }
        } catch (ClassNotFoundException ex) {

            Logger.getLogger(orden_produccion.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(orden_produccion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

