    private void grillaBancoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_grillaBancoMouseClicked
       int fila=grillaBanco.getSelectedRow();
        
       String cod= grillaBanco.getValueAt(fila, 0).toString();
       String descripcion= grillaBanco.getValueAt(fila, 1).toString();
       String medida= grillaBanco.getValueAt(fila, 2).toString();
       String precio= grillaBanco.getValueAt(fila, 3).toString();
       String iva= grillaBanco.getValueAt(fila, 4).toString();
       
       if(busqueda.equals("pediCompra") && operacion.equals("")){   //primero se realiza esta accion porque de otro modo vacia la variable "operacion"
           pedido_de_compra.txtCodArti.setText(cod);
           pedido_de_compra.txtArti.setText(descripcion);
           pedido_de_compra.txtArti.requestFocus();
           busqueda = "";
           dispose();
       }
       if(busqueda.equals("pediMateria") && operacion.equals("")){   //primero se realiza esta accion porque de otro modo vacia la variable "operacion"
           pedido_de_materia_prima.txtCodArti.setText(cod);
           pedido_de_materia_prima.txtArti.setText(descripcion);
           pedido_de_materia_prima.txtArti.requestFocus();
           busqueda = "";
           dispose();
       }
       if(busqueda.equals("compra") && operacion.equals("")){   //primero se realiza esta accion porque de otro modo vacia la variable "operacion"
           compras.txt_cod_arti.setText(cod);
           compras.txt_arti.setText(descripcion);
           compras.txt_arti.requestFocus();
           busqueda = "";
           dispose();
       }
       if(busqueda.equals("presuProvee") && operacion.equals("")){   //primero se realiza esta accion porque de otro modo vacia la variable "operacion"
           presupuesto_de_compra.txtCodArti.setText(cod);
           presupuesto_de_compra.txtArti.setText(descripcion);
           presupuesto_de_compra.txtArti.requestFocus();
           busqueda = "";
           dispose();
       }
        if(busqueda.equals("notaRemision") && operacion.equals("")){   //primero se realiza esta accion porque de otro modo vacia la variable "operacion"
           notaRemision.txtcodigo.setText(cod);
           notaRemision.txt_nombre_arti.setText(descripcion);
           notaRemision.txt_nombre_arti.requestFocus();
           busqueda = "";
           dispose();
       }
       
      
       
       if(operacion.equals("modificar") || operacion.equals("borrar")){
           txtCodigo.setText(cod);
           txtDescri.setText(descripcion);
           txtMedida.setText(medida);
           txtPrecio.setText(precio);
           txtIva.setText(iva);
       }
       
       if(operacion.equals("modificar")){
           txtDescri.requestFocus();
           
       }
       if(operacion.equals("borrar")){
           btnGrabar.doClick();
       }
       
       //consulta de orden de produccion
       if(busqueda.equals("orden_produccion")){
           orden_produccion.codprodu1.setText(cod);
           orden_produccion.codprodu1.setEnabled(false);
           orden_produccion.desprod1.setText(descripcion);
           orden_produccion.cant1.setText("1");
           orden_produccion.cant1.setEnabled(true);
           orden_produccion.cant1.requestFocus();
           dispose();
           
                   
       }
       
    }//GEN-LAST:event_grillaBancoMouseClicked

