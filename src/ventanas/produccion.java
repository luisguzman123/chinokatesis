    private void cargarProductos() {
        Conexion cn = new Conexion();

        try {
            cn.conectar();
            ResultSet producto = cn.consultar("SELECT\n"
                    + "p.pro_cod,\n"
                    + "p.pro_desc,\n"
                    + "dp.cantidad\n"
                    + "FROM detalle_orden_produccion dp\n"
                    + "JOIN producto p\n"
                    + "ON p.pro_cod = dp.pro_cod\n"
                    + "WHERE dp.cod_or_prod = " + nroorden.getText()); //order by ordena de menor a mayor, si se quiere de mayor a menor se le agrega desc al final

            Metodos.limpiarTabla(grilla);
            if (producto.isBeforeFirst()) {
                while (producto.next()) {
                    Metodos.cargarTabla(grilla, new Object[]{
                        producto.getInt("pro_cod"),
                        producto.getString("pro_desc"),
                        producto.getInt("cantidad")
                    });
                }
            }

            //cargamos la cabecera con los depositos
            String deposito = "SELECT \n"
                    + "dp.cod_depo,\n"
                    + "d.depo_desc\n"
                    + "FROM detalle_orden_produccion dp\n"
                    + "JOIN deposito d \n"
                    + "ON d.cod_depo =  dp.cod_depo\n"
                    + "WHERE dp.cod_or_prod = " + nroorden.getText() + " LIMIT 1";
            ResultSet deposito_rs = cn.consultar(deposito);
            if (deposito_rs.isBeforeFirst()) {
                while (deposito_rs.next()) {
                    id_deposito_txt.setText(String.valueOf(deposito_rs.getInt("cod_depo")));
                    deposito_nombre_lbl.setText(deposito_rs.getString("depo_desc"));
                }
            }

        } catch (ClassNotFoundException ex) {

            Logger.getLogger(orden_produccion.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(orden_produccion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

