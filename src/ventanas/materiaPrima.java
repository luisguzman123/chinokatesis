    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        int respuesta = 5;
        duplicado = Metodos.evitarDuplicado("materia_prima", "mat_desc", txtPrecio.getText().trim(), "cod_materia", txtCodigo.getText());
        if (duplicado && (!operacion.equals("borrar"))) {
            JOptionPane.showMessageDialog(null, "Este registro ya esta en la base de datos");
            btnCancelar.doClick();
        }else{
            respuesta=JOptionPane.showConfirmDialog(null, confirmar);
        }
        if (respuesta==0) {
             String sql="";
            if(operacion.equals("agregar")){
                sql="Insert into materia_prima (mat_desc, medida, mat_precio, iva) "
                + "values ('"+txtDescri.getText().toUpperCase().trim()+"', '"+txtMedida.getText().toUpperCase()+"',"+txtPrecio.getText().toUpperCase()+","+txtIva.getText().toUpperCase()+")";
            }
            
            if(operacion.equals("modificar")){
                sql="update materia_prima set mat_desc = '"+txtDescri.getText().toUpperCase().trim()+"', medida = '"+txtMedida.getText().toUpperCase()+"', mat_precio = "+txtPrecio.getText().toUpperCase()+", iva = "+txtIva.getText().toUpperCase()+"  where cod_materia = "+txtCodigo.getText();
            }
            
            if(operacion.equals("borrar")){
                sql="Delete from materia_prima where cod_materia = "+txtCodigo.getText();
            }
            
            Conexion cn=new Conexion();
            try {
                cn.conectar();
                System.out.println(sql);
                cn.actualizar(sql);
                JOptionPane.showMessageDialog(null, mensaje);
                btnCancelar.doClick();

                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(materiaPrima.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, ex.getMessage());
                
            } catch (SQLException ex) {
                Logger.getLogger(materiaPrima.class.getName()).log(Level.SEVERE, null, ex);
                if(operacion.equals("borrar") && txtCodigo.getText().equals("")){
                    JOptionPane.showMessageDialog(null, "Primero debe seleccionar una materia prima");
                }
                if (operacion.equals("borrar") && !txtCodigo.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "No se puede eliminar, registro referenciado");
                }
                if (operacion.equals("modificar")) {
                    JOptionPane.showMessageDialog(null, ex);
                }
//                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }else if(respuesta==1 || respuesta==2){
            JOptionPane.showMessageDialog(null, "Cancelado");
            btnCancelar.doClick();
        }
    }//GEN-LAST:event_btnGrabarActionPerformed

