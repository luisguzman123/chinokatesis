    public void getDatos(){
        Conexion cn=new Conexion();
        
        try {
            cn.conectar();
            ResultSet vehiculo=cn.consultar("select * from materia_prima where mat_desc ilike '%"+txtBuscador.getText()+"%' order by cod_materia"); //order by ordena de menor a mayor, si se quiere de mayor a menor se le agrega desc al final
            Metodos.limpiarTabla(grillaBanco);
            if(vehiculo.isBeforeFirst()){
                while(vehiculo.next()){
                    Metodos.cargarTabla(grillaBanco, new Object[]{vehiculo.getString("cod_materia"), vehiculo.getString("mat_desc"), vehiculo.getString("medida"), vehiculo.getString("mat_precio"), vehiculo.getString("iva")});
                }
            }else{
                JOptionPane.showMessageDialog(null, "No hay registros en la base de datos");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(materiaPrima.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "No se encuentra "+ex.getMessage());
        } catch (SQLException ex) {
            Logger.getLogger(materiaPrima.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null,ex.getMessage());
        }
    }
    
    
    
    
    
